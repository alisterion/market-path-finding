const grid = new PF.Grid(walls);
const finder = new PF.AStarFinder();

let DRAW_MODE = false;


const graphics = [];


// load the texture we need
PIXI.Loader.shared.add('schema', 'assets/img/market1.png').load((loader, resources) => {
    const schema = new PIXI.Sprite(resources.schema.texture);
    const scale = 0.6;
    schema.scale.set(scale, scale);
    app.stage.addChild(schema);


    const step = 10;

    let x = 0;
    let y = 0;

    for (let j = 0; j < AREA_HEIGHT; j += step) {
        graphics[x] = [];
        blocks[x] = [];
        y = 0;
        for (let i = 0; i < AREA_WIDTH; i += step) {
            graphics[x][y] = new PIXI.Graphics();
            const cell = graphics[x][y];

            if (walls[x][y]) {
                blocks[x][y] = 1;
                cell.alpha = 0;
                cell.selected = true;
                cell.isWall = true;
                cell.beginFill(0xfff000);
            } else {
                blocks[x][y] = 0;
                cell.alpha = 0;
                cell.beginFill(0xccfccc);
            }

            cell.lineStyle(1, 0x000000, 1);
            cell.drawRect(0, 0, step, step);
            cell.position.x = i;
            cell.position.y = j;
            cell.interactive = true;


            cell.cordsX = x;
            cell.cordsY = y;

            cell
                .on('mouseover', onButtonOver)
                .on('mousedown', onButtonDown)
                .on('mouseup', onButtonUp);
            y++;
            cell.endFill();
            app.stage.addChild(cell);
        }
        x++;
    }


});


let MOUSE_IS_DOWN = false;

let START = [];
let END = [];

let lastPath = [];


function renderPath() {
    const path = finder.findPath(START[1], START[0], END[1], END[0], grid);
    const pathLength = path.length;

    for (let i = 0; i < pathLength; i += 1) {
        graphics[path[i][1]][path[i][0]].alpha = 1;
    }
    lastPath = path;
}

function clearPath() {
    const pathLength = lastPath.length;

    for (let i = 0; i < pathLength; i += 1) {
        graphics[lastPath[i][1]][lastPath[i][0]].alpha = 0;
    }
    lastPath = [];
}

let START_SELECTED = false;

function onButtonDown() {

    if (DRAW_MODE) {
        MOUSE_IS_DOWN = true;
        if (this.isWall) {
            return;
        }
        if (this.selected) {
            this.alpha = 0;
            this.selected = false;
            blocks[this.cordsX][this.cordsY] = 0;
        } else {
            this.alpha = 1;
            this.selected = true;
            blocks[this.cordsX][this.cordsY] = 1;
        }
    } else {

        if (this.isWall) {
            return;
        }

        this.alpha = 1;

        if (!START_SELECTED && !END.length && !START.length) {
            clearPath()
        }

        if (START_SELECTED && !END.length)
            END = [this.cordsX, this.cordsY];

        if (!START.length)
            START = [this.cordsX, this.cordsY];
        START_SELECTED = true;


        if (START.length > 0 && END.length > 0) {
            renderPath();
            START_SELECTED = false;
            START = [];
            END = [];
        }

    }
}

function onButtonUp() {
    MOUSE_IS_DOWN = false;
}

function onButtonOver() {
    if (this.isWall) {
        return;
    }
    if (MOUSE_IS_DOWN) {
        if (this.selected) {
            this.alpha = 0;
            this.selected = false;
            blocks[this.cordsX][this.cordsY] = 0;
        } else {
            this.alpha = 1;
            this.selected = true;
            blocks[this.cordsX][this.cordsY] = 1;

        }

    }
}
